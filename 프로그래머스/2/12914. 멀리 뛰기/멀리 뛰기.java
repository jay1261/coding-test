import java.util.*;

class Solution {
    public long solution(int n) {
        ArrayList<Long> list = new ArrayList<Long>();
        list.add(1L);
        list.add(1L);
        
        for(int i = 2; i <= n; i++){
            long a = list.get(i-1);
            long b = list.get(i-2);
            list.add((a+b) % 1234567);
        }
        
        return list.get(n) ;
    }
}


